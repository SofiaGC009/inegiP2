    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
     // Obtener el usuario y la contraseña ingresados por el usuario
    String usuario = txtUsuario.getText();
    String password = new String(txtPassword.getPassword());

    // Crear una instancia de Administrador y llamar al método login
    Administrador admin = new Administrador(usuario, password);
    if (admin.login()) {
        // Inicio de sesión exitoso
        // Abre la nueva ventana o realiza otras acciones
        Menu menu = new Menu(); // Suponiendo que "Principal" es tu clase para la ventana principal
        menu.setVisible(true); // Muestra la nueva ventana
        dispose(); // Cierra la ventana actual de inicio de sesión
    } else {
        // Inicio de sesión fallido
        // Muestra un mensaje de error al usuario
        JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnIngresarActionPerformed

